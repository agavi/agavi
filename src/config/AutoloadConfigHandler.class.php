/**
 * AgaviAutoloadConfigHandler allows you to specify a list of classes that will
 * automatically be included for you upon first use.
 *
 * @package    agavi
 * @subpackage config
 *
 * @author     Sean Kerr <skerr@mojavi.org>
 * @author     Dominik del Bondio <ddb@bitxtender.com>
 * @copyright  (c) Authors
 * @since      0.9.0
 *
 * @version    $Id$
 */
class AgaviAutoloadConfigHandler extends AgaviConfigHandler
{
	/**
	 * Execute this configuration handler.
	 *
	 * @param      string An absolute filesystem path to a configuration file.
	 * @param      string Name of the executing context (if any).
	 *
	 * @return     string Data to be written to a cache file.
	 *
	 * @throws     <b>AgaviUnreadableException</b> If a requested configuration file
	 *                                             does not exist or is not readable.
	 * @throws     <b>AgaviParseException</b> If a requested configuration file is
	 *                                        improperly formatted.
	 *
	 * @author     Sean Kerr <skerr@mojavi.org>
	 * @author     Dominik del Bondio <ddb@bitxtender.com>
	 * @since      0.9.0
	 */
	public function execute($config, $context = null)
	{

		$data = $this->parseFile($config);
		if(($sysConfDir = AgaviConfig::get('core.system_config_dir'))) {
			$data = array_merge($this->parseFile($sysConfDir . '/autoload.xml'), $data);
		}

		$code = 'Agavi::setAutoloads(' . var_export($data, true) . ');';

		// compile data
		$retval = "<?php\n" .
				  "// auth-generated by AutoloadConfigHandler\n" .
				  "// date: %s\n%s\n?>";
		$retval = sprintf($retval, date('m/d/Y H:i:s'), $code);

		return $retval;

	}


	protected function parseFile($config)
	{
		// parse the config file
		$conf = AgaviConfigCache::parseConfig($config, false);

		$data = array();
		$environment = AgaviConfig::get('core.environment');
		foreach($conf->configurations as $cfg)
		{
			$env = $cfg->hasAttribute('environment') ? $cfg->getAttribute('environment') : $environment;

			if($env != $environment)
				continue;

			// let's do our fancy work
			foreach($cfg->autoloads as $entry) {

				$file = $this->replaceConstants($entry->getValue());
				$class = $entry->getAttribute('name');

				if(!($fp = @fopen($file, 'r', true))) {

					// the class path doesn't exist
					$error = 'Configuration file "%s" specifies class "%s" with ' .
							 'nonexistent or unreadable file "%s"';
					$error = sprintf($error, $config, $class, $file);

					throw new AgaviParseException($error);
				}
				fclose($fp);

				$data[$class] = $file;
			}
		}
		return $data;
	}

}

